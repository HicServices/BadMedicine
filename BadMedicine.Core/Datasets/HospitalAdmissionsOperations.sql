
SELECT 
count(*) CountOfRecords,
[ISD_SMR]..[SMR01].[MAIN_CONDITION],
[ISD_SMR]..[SMR01].[MAIN_OPERATION],
[ISD_SMR]..[SMR01].[MAIN_OPERATION_B],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_1],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_1B],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_2],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_2B],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_3],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_3B]
FROM 
[ISD_SMR]..[SMR01] GROUP BY 
[ISD_SMR]..[SMR01].[MAIN_CONDITION],
[ISD_SMR]..[SMR01].[MAIN_OPERATION],
[ISD_SMR]..[SMR01].[MAIN_OPERATION_B],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_1],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_1B],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_2],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_2B],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_3],
[ISD_SMR]..[SMR01].[OTHER_OPERATION_3B]
having count(*)>5
order by [ISD_SMR]..[SMR01].[MAIN_CONDITION]